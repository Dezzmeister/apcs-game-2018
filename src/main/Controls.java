package main;

import java.awt.event.KeyEvent;

/**
 * Contains controls for a Game. 
 *
 * @author Joe Desmond
 */
public class Controls {
	public int moveLeft = KeyEvent.VK_A;
	public int moveRight = KeyEvent.VK_D;
	public int moveForward = KeyEvent.VK_W;
	public int moveBackward = KeyEvent.VK_S;
	public int sprint = KeyEvent.VK_SHIFT;
	public int pause = KeyEvent.VK_P;
	public int screenshot = KeyEvent.VK_I;
}
